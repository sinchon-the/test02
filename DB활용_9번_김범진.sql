CREATE TABLE DEPT 
(
  DEPTNO NUMBER NOT NULL 
, DNAME VARCHAR2(14) 
, LOC VARCHAR2(13) 
, CONSTRAINT DEPT_PK PRIMARY KEY 
  (
    DEPTNO 
  )
  USING INDEX 
  (
      CREATE UNIQUE INDEX DEPT_PK1 ON DEPT (DEPTNO ASC) 
  )
  ENABLE 
);

CREATE TABLE EMP 
(
  EMPNO NUMBER NOT NULL 
, ENAME VARCHAR2(10) 
, JOB VARCHAR2(9) 
, MGR NUMBER 
, HIREDATE DATE 
, SAL NUMBER 
, COMM NUMBER 
, DEPTNO NUMBER 
, CONSTRAINT EMP_PK PRIMARY KEY 
  (
    EMPNO 
  )
  USING INDEX 
  (
      CREATE UNIQUE INDEX EMP_PK1 ON EMP (EMPNO ASC) 
  )
  ENABLE 
);

ALTER TABLE EMP
ADD CONSTRAINT EMP_FK1 FOREIGN KEY
(
  DEPTNO 
)
REFERENCES DEPT
(
  DEPTNO 
)
ENABLE;

CREATE VIEW EMPLIST
AS SELECT 
    empno,ename, job,dname
FROM 
    emp,dept
where
    emp.deptno=dept.deptno;
